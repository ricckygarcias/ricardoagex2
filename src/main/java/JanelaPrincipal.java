    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        Locale localeBR = new Locale("pt","BR");
        NumberFormat dinheiro = NumberFormat.getCurrencyInstance(localeBR);
        
        String nome = txtnome.getText();
        Double distancia = Double.valueOf(txtdistancia.getText());
        Double consumo = Double.valueOf(txtconsumo.getText());
        Double preco = Double.valueOf(txtpreco.getText());
        Double resultado = distancia / consumo * preco;
        
        lblresultado.setText(nome + ", o total gasto foi de: " + dinheiro.format(resultado));
        
    }//GEN-LAST:event_jButton1ActionPerformed

